package com.company;

public class Stereo {
    private int maxDecibels;
    private int numSpeakers;
    private int width;
    private int height;
    private int depth;

    public Stereo(int maxDecibels, int numSpeakers, int width, int height, int depth) {
        this.maxDecibels = maxDecibels;
        this.numSpeakers = numSpeakers;
        this.width = width;
        this.height = height;
        this.depth = depth;
    }
}
